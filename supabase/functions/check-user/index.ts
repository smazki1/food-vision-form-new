import "jsr:@supabase/functions-js/edge-runtime.d.ts"

import { serve } from 'https://deno.land/std@0.177.0/http/server.ts'
import { createClient } from 'https://esm.sh/@supabase/supabase-js@2'

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
}

console.log("Hello from Functions!")

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders })
  }

  try {
    const { email } = await req.json()

    const supabaseClient = createClient(
      Deno.env.get('SUPABASE_URL') ?? '',
      Deno.env.get('SUPABASE_ANON_KEY') ?? ''
    )

    // Try to sign up with the email to check if it exists
    const { data, error } = await supabaseClient.auth.signUp({
      email,
      password: 'temporary-password-' + Math.random(),
    })

    if (error && error.message === 'User already registered') {
      // User exists, now check if they're confirmed
      const { data: { session }, error: signInError } = await supabaseClient.auth.signInWithPassword({
        email,
        password: 'wrong-password'
      })

      return new Response(
        JSON.stringify({
          exists: true,
          confirmed: true, // If we can try to sign in, the user is confirmed
          error: null
        }),
        { 
          headers: { ...corsHeaders, 'Content-Type': 'application/json' },
          status: 200 
        }
      )
    }

    if (error) {
      throw error
    }

    // If we get here, the user doesn't exist
    return new Response(
      JSON.stringify({
        exists: false,
        confirmed: false,
        error: null
      }),
      { 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 200 
      }
    )

  } catch (error) {
    return new Response(
      JSON.stringify({ error: error.message }),
      { 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 400 
      }
    )
  }
})

